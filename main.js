let show_watch = document.createElement("div");
if (window.localStorage.getItem("Watched")) {
  newArr = JSON.parse(localStorage.getItem("Watched"));
}

btn.onclick = () => {
  let remainingItems = arrryOfitems.filter((item) => !newArr.includes(item));

  if (remainingItems.length === 0) {
    return (fieldRandom.textContent = `All the list has been watched`);
  }

  let random = Math.floor(Math.random() * remainingItems.length);
  let randomItem = remainingItems[random];

  newArr.push(randomItem);
  fieldRandom.textContent = randomItem;
  localStorage.setItem("Watched", JSON.stringify(newArr));
  counter_left.textContent = `Number Of Shows left: ${
    arrryOfitems.length - newArr.length
  }`;

  show_watch.textContent = `Wathed Shows: ${newArr.length}`;
};

